declare(strict_types=1);

namespace DpdShipping\Controller\Configuration;

if (!defined('_PS_VERSION_')) {
    exit;
}

use PrestaShopBundle\Controller\Admin\FrameworkBundleAdminController;
use Symfony\Component\HttpFoundation\Request;
use Symfony\Component\HttpFoundation\Response;

class DpdShippingOnboardingController extends FrameworkBundleAdminController
{
    public function __construct()
    {
        parent::__construct();
    }

    public function index(Request $request): Response
    {
        $textFormDataHandler = $this->get('prestashop.module.dpdshipping.form.dpdshipping_onboarding_form_data_handler');

        $textForm = $textFormDataHandler->getForm();
        $textForm->handleRequest($request);

        if ($textForm->isSubmitted() && $textForm->isValid()) {
            $errors = $textFormDataHandler->save($textForm->getData());

            if (empty($errors)) {
                $this->addFlash('success', $this->trans('Successful update.', 'Admin.Notifications.Success'));

                return $this->redirectToRoute('dpdshipping_connection_form');
            }

            $this->flashErrors($errors);
        }

        return $this->render('@Modules/dpdshipping/views/templates/admin/configuration/onboarding-form.html.twig', [
            'form' => $textForm->createView(),
        ]);
    }
}
